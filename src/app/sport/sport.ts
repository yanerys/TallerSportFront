/**
* This class represents a sport. 
* It contains all the information relevant to the sport.
*/
export class Sport {
    /**
    * The sport's id
    */
    id: number;

    /**
    * The sport's name
    */
    name: string;


    /**
    * A brief summary of the sport
    */
    description: string;

    /**
    * The location of the sport's image
    */
    image: string;
}
