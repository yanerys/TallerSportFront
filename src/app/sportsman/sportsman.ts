/**
 * This class represents an sportsman. 
 * It contains all the information relevant to the sportsman.
 */
export class Sportsman {
    /**
    * The sportsman's id
    */
    id: number;

    /**
     * The sportsman's name
     */ 
    name: string;

    /**
     * The location of the sportsman's profile picture
     */
    image: string;

    /**
     * The sportsman's birthdate MM/DD/YYYY
     */
    birthDate: string;
    
}
